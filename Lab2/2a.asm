DATA SEGMENT
  ARRAY1 DW 11H,22H,33H,44H,55H, 2 DUP (0);
  COUNT DW 05H 
DATA ENDS

CODE SEGMENT
  ASSUME CS:CODE,DS:DATA

START:
  MOV AX,DATA
  MOV DS,AX
  
  LEA SI,ARRAY1; Load SI with EA of ARRAY1 in ds
  INC SI,8
  MOV DI,SI
  INC DI,4

  MOV CH, 00H
  MOV CL,COUNT

NEXT:
  MOV AX,[SI]
  MOV [DI],AX
  DEC SI
  DEC SI
  DEC DI
  DEC DI
  LOOP NEXT
  
  MOV AH,4CH
  INT 21H

CODE ENDS
END START